public class Emplacement {
    // ATTRIBUTS
    private int x;
    private int y;
    
    // CONSTRUCTEURS
    public Emplacement (int x, int y){
        x = this.x;
        y = this.y;
    }
    
    // METHODES
    
    
    // GETTERS

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
