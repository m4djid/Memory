public class Emplacement {
    
    // ATTRIBUTS
    private int x;
    private int y;
    
    // CONSTRUCTEURS
    public Emplacement(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    // GETTERS
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
